// 合并词典（TECH_DICT 中的词条会覆盖 DICTIONARY 中同名词条，以技术释义为准）
const FULL_DICT = Object.assign({}, DICTIONARY, TECH_DICT);

/**
 * 批量查询单词释义
 * 遍历单词数组，从内置词典中查找每个单词的释义
 * 忽略大小写，先将单词转为小写再查询
 * 
 * @param {Array<string>} words - 待查询的单词数组
 * @returns {Object} { meanings: { word: meaning }, notFound: [] }
 *   - meanings: 已找到释义的单词映射表
 *   - notFound: 未在词典中找到的单词列表
 * 
 * 示例：
 *   lookupBatch(['algorithm', 'unknownword'])
 *   // => { meanings: { algorithm: '算法；计算程序' }, notFound: ['unknownword'] }
 */
function lookupBatch(words) {
  const meanings = {};
  const notFound = [];

  // 遍历每个单词，查词典
  for (let i = 0; i < words.length; i++) {
    const word = words[i];
    // 转为小写以支持不区分大小写查询
    const lower = String(word).trim().toLowerCase();
    if (FULL_DICT[lower]) {
      meanings[lower] = FULL_DICT[lower];
    } else {
      notFound.push(lower);
    }
  }

  return { meanings: meanings, notFound: notFound };
}

